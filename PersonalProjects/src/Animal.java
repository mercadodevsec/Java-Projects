abstract class Animal {
    private String name;
    private String type;
    private String color;
    private int numLegs;

    public Animal() {
    }
    public Animal(String name, String type, String color, int numLegs) {
        this.name = name;
        this.type = type;
        this.color = color;
        this.numLegs = numLegs;
    }

}
